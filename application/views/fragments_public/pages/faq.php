<script>
  function openTopic(evt, topic) {
    var i, subtopic, faqlinks;

    subtopic = document.getElementsByClassName("subtopic");
    for(i = 0; i < subtopic.length; i++){
      subtopic[i].style.display = "none";
    }

    faqlinks = document.getElementsByClassName("faqlinks");
    for(i = 0; i <faqlinks.length; i++){
      faqlinks[i].className = faqlinks[i].className.replace(" active", "");
    }
    
    document.getElementById(topic).style.display = "inline-block";
    evt.currentTarget.className += " acive";
    
  }
</script>
  

  




